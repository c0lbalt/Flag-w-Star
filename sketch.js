function setup() {
  createCanvas(400, 400);
  background("teal");
}

function draw() {
  
    // Set the outline to white
    stroke(255); // White color
    strokeWeight(2); // Thickness of the outline
  
    // Draw shapes with white outline
    fill("green"); // Fill color for the shape
    ellipse(200, 200, 100, 100); // Draw a circle
  
    
  }
  

